
public class Main
{   
	public static void main(String[] args) {
	//while
    while (true) {
        System.out.println("Это бесконечный цикл с использованием while.");
    }   
    
    //do-while
    do {
        System.out.println("Это бесконечный цикл с использованием do-while.");
    } while (true);
	}
}